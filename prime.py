def is_prime(num):
    if num <= 1:
        return False
    for i in range(2, int(num ** 0.5) + 1):
        if num % i == 0:
            return False
    return True

def find_primes(start, end):
    primes = []
    for num in range(start, end + 1):
        if is_prime(num):
            primes.append(num)
    return primes

# Find all prime numbers from 1 to 100
prime_numbers = find_primes(1, 100)
print("Prime numbers from 1 to 100:", prime_numbers)

